    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        String pid = pi1.getText();
        String pname = pn.getText();
        String pgender = pg1.getText();
        String pillness = pi2.getText();
        String page= pa1.getText();
        String pdate= dat.getText();
        String ptime= tim.getText();

        
        try{
            
            Class.forName("com.mysql.jdbc.Driver");
            
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/hospital","root","");
            String sql = "UPDATE patient_record SET NAME='" + pname + "', GENDER='" + pgender + "',ILLNESS='" + pillness + "', AGE='" + page + "',ADMITDATE='" + pdate + "' ,ADMITTIME='" + ptime + "'WHERE id='" + pid + "'";

            PreparedStatement ptst =conn.prepareStatement(sql);
            ptst.execute();

            JOptionPane.showMessageDialog(null,"Data Upddated Successfully");

           pi1.setText("");pn.setText("");pg1.setText("");
            pi2.setText("");pa1.setText("");dat.setText("");tim.setText("");
        }catch(HeadlessException | ClassNotFoundException | SQLException e){
            
            
                        JOptionPane.showMessageDialog(null, e);

        }

    }//GEN-LAST:event_jButton2ActionPerformed


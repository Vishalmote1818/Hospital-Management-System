    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
         
        String ID = pi1.getText();
        
         try{
             
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/hospital","root","");
            Statement st = conn.createStatement();
             ResultSet rs = st.executeQuery("select * from patient_record WHERE id='"+ID+"'");
           

             if(rs.next()){
                 pn.setText(rs.getString(2));
                 pm.setText(rs.getString(3));
                 pa1.setText(rs.getString(4));
                 pg1.setText(rs.getString(5));
                 pi2.setText(rs.getString(6));
                 dat.setText(rs.getString(7));
                 tim.setText(rs.getString(8 ));

             }
             
             else{
            JOptionPane.showMessageDialog(null, "Patient ID does not exist"); 
             }
         }catch(Exception e){
             
         }
        
        
        
        
        
    }//GEN-LAST:event_jButton4ActionPerformed


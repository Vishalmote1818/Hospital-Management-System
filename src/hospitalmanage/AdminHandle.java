    st.executeUpdate(createTableSql1);
    
    
    
    // Table for the doctor record
    String createTableSql2 = "CREATE TABLE IF NOT EXISTS doctor_record(ID INT PRIMARY KEY, NAME VARCHAR(255) NOT NULL ,GENDER VARCHAR(255) NOT NULL , SPECIALIZATION VARCHAR(255) NOT NULL ,MOBILENO VARCHAR(255) NOT NULL , CITY VARCHAR(255) NOT NULL)";
    st.executeUpdate(createTableSql2);
    
    
    
    
     // Table for the PATIENT RECORD 

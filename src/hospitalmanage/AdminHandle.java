    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
         try {
            // Replace placeholders with your actual database credentials
            String url = "jdbc:mysql://localhost:3306/hospital";
            String user = "root";
            String password = "";

             // Establish connection
             Connection connection = DriverManager.getConnection(url, user, password);
            
            String tableName = tableNameField.getText().trim(); // Get table name from the text field
            if (!tableName.isEmpty()) {
                // Create SQL statement to delete the entire table
                String sql = "DROP TABLE " + tableName;

                // Create statement
                Statement statement = connection.createStatement();

                // Execute the statement
                statement.executeUpdate(sql);

                // Show success message
                JOptionPane.showMessageDialog(this, "Table deleted successfully.");
            } else {
                JOptionPane.showMessageDialog(this, "Please enter a table name.");
            }
            
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error deleting table: " + e.getMessage());
        }
        
        
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

